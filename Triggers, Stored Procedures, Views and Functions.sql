Triggers:
A trigger is a stored procedure that runs automatically when various events happen.


Stored Procedure:
A stored procedure is a user defined piece of code written in the local version of PL/SQL, which may return a value (making it a function) that is invoked by calling it explicitly.


Views:
VIEW is a virtual table, through which a selective portion of the data from one or more tables can be seen.A view do not contain data of their own.


Functions:
A function is a set of SQL statements that perform a specific task.A function accepts inputs in the form of parameters and returns a value.
